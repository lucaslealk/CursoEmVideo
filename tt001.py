n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
s = n1 + n2 
st = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2 
ex = n1 ** n2
print('Fizemos as contas e a soma é {}, a subtração ficou {}, a multiplicação \
      é {}, a divisão ficou desse jeito {}, a divisão inteira é essa {} \
      e a exponenciação é {} '.format(s, st, m, d, di, ex))
